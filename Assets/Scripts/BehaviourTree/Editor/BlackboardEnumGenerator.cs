#if UNITY_EDITOR

using System.Collections.Generic;
using System.IO;
using System.Text;
using System.Text.RegularExpressions;
using BehaviourTree.Core;
using UnityEditor;
using UnityEngine;

namespace BehaviourTree.Editor
{
    /// <summary>
    /// Scans all BlackboardDefinition assets and regenerates <c>BlackboardEnums.cs</c>
    /// with one enum per definition for zero-cost typed blackboard access.
    /// </summary>
    public static class BlackboardEnumGenerator
    {
        private const string FilePath = "Assets/Scripts/BehaviourTree/Runtime/Execution/Generated/BlackboardEnums.cs";

        [MenuItem("BehaviourTree/Generate Blackboard Enums", priority = 31)]
        public static void Generate()
        {
            var sb = new StringBuilder();
            sb.AppendLine("// ═══════════════════════════════════════════════════════════════════");
            sb.AppendLine("// AUTO-GENERATED by BlackboardEnumGenerator. Do not edit manually.");
            sb.AppendLine("// Regenerate via Tools → BehaviourTree → Generate Blackboard Enums.");
            sb.AppendLine("// ═══════════════════════════════════════════════════════════════════");
            sb.AppendLine();
            sb.AppendLine("namespace BehaviourTree.Runtime");
            sb.AppendLine("{");

            var definitions = FindAllDefinitions();
            var usedEnumNames = new HashSet<string>();

            foreach (var def in definitions)
            {
                if (def == null || def.sharedVariables == null || def.sharedVariables.Count == 0)
                    continue;

                string rawName = def.name;
                // Strip trailing _BB_Definition if present
                if (rawName.EndsWith("_BB_Definition"))
                    rawName = rawName.Substring(0, rawName.Length - "_BB_Definition".Length);

                string enumName = SanitizeIdentifier(rawName) + "_BB_Keys";

                // Deduplicate
                string uniqueName = enumName;
                int suffix = 1;
                while (!usedEnumNames.Add(uniqueName))
                {
                    uniqueName = enumName + "_" + suffix;
                    suffix++;
                }

                sb.AppendLine($"    public enum {uniqueName} : int");
                sb.AppendLine("    {");

                for (int i = 0; i < def.sharedVariables.Count; i++)
                {
                    string varName = SanitizeIdentifier(def.sharedVariables[i].name);
                    // Edge case: name sanitizes to empty or starts with digit
                    if (string.IsNullOrEmpty(varName))
                        varName = $"Var_{i}";
                    if (char.IsDigit(varName[0]))
                        varName = "_" + varName;

                    sb.AppendLine($"        {varName} = {i}{(i < def.sharedVariables.Count - 1 ? "," : "")}");
                }

                sb.AppendLine("    }");
                sb.AppendLine();
            }

            sb.AppendLine("}");

            string newContent = sb.ToString();

            if (File.Exists(FilePath))
            {
                string existing = File.ReadAllText(FilePath);
                if (existing == newContent)
                    return;
            }

            string dir = Path.GetDirectoryName(FilePath);
            if (!string.IsNullOrEmpty(dir) && !Directory.Exists(dir))
                Directory.CreateDirectory(dir);

            File.WriteAllText(FilePath, newContent);
            AssetDatabase.ImportAsset(FilePath);
            Debug.Log("BlackboardEnums regenerated at: " + FilePath);
        }

        private static List<BlackboardDefinition> FindAllDefinitions()
        {
            var results = new List<BlackboardDefinition>();
            string[] guids = AssetDatabase.FindAssets("t:BlackboardDefinition");
            foreach (string guid in guids)
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                var asset = AssetDatabase.LoadAssetAtPath<BlackboardDefinition>(path);
                if (asset != null)
                    results.Add(asset);
            }
            return results;
        }

        /// <summary>
        /// Replaces any character that is not a letter, digit, or underscore with an underscore.
        /// </summary>
        private static string SanitizeIdentifier(string name)
        {
            if (string.IsNullOrEmpty(name))
                return name;
            return Regex.Replace(name, @"[^a-zA-Z0-9_]", "_");
        }
    }
}

#endif
